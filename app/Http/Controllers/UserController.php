    {
        $role=2;
        $status=0;
        $user = new User([
            'name' => $request->get('name'),
            'gender' => $request->get('gender'),
            'age' => $request->get('age'),
            'email' => $request->get('email'),
            'password' => $request->get('password'),
            'role_id' => $role,
            'status' => $status

        ]);
        $user->save();
        return view('login');
